async function seedMerchants() {
    try {
        await dbConnect();
        console.log("Connected to database");

        // Clear existing demo merchants
        await Partner.deleteMany({ merchantId: { $regex: /^demo/ } });
        console.log("Cleared existing demo merchants");

        // Insert new demo merchants
        const inserted = await Partner.insertMany(demoMerchants);
        console.log(`Inserted ${inserted.length} demo merchants`);

        console.log("Seeding completed successfully");
    } catch (error) {
        console.error("Error seeding merchants:", error);
    } finally {
        process.exit(0);
    }
}

seedMerchants();
